import calcEquation from "./calcEquation";

test("1", () => {
  expect(calcEquation([ [ "a", "b" ], [ "b", "c" ] ], [ 2.0, 3.0 ], [ [ "a", "c" ], [ "b", "a" ], [ "a", "e" ], [ "a", "a" ], [ "x", "x" ] ])).toStrictEqual([ 6.00000, 0.50000, -1.00000, 1.00000, -1.00000 ])
});
test("2", () => {
  expect(calcEquation([ [ "a", "b" ], [ "b", "c" ], [ "bc", "cd" ] ], [ 1.5, 2.5, 5.0 ], [ [ "a", "c" ], [ "c", "b" ], [ "bc", "cd" ], [ "cd", "bc" ] ])).toStrictEqual([ 3.75000, 0.40000, 5.00000, 0.20000 ])
});
test("3", () => {
  expect(calcEquation([["a","b"],["c","b"],["d","c"]],[1,2,4],[["a","c"]])).toStrictEqual([ 0.5 ])
});
test("4", () => {
  expect(calcEquation([["x1","x2"],["x2","x3"],["x1","x4"],["x2","x5"]],
    [3.0,0.5,3.4,5.6],
    [["x2","x4"],["x1","x5"],["x1","x3"],["x5","x5"],["x5","x1"],["x3","x4"],["x4","x3"],["x6","x6"],["x0","x0"]])).toStrictEqual(
    [1.13333,16.80000,1.50000,1.00000,0.05952,2.26667,0.44118,-1.00000,-1.00000]
  )
});
